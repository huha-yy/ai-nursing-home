def normalize_lang(raw: str | None) -> str:
    """Map a raw cookie value to a supported language code.

    Unknown / missing / malformed → DEFAULT_LANG. Never raises.
    """
    if raw in LANGS:
        return raw  # type: ignore[return-value]
    return DEFAULT_LANG


def translate(lang: str, key: str) -> str:
    """Look up a message. Missing key → returns the key itself (dev-visible)."""
    lang = normalize_lang(lang)
    return MESSAGES[lang].get(key, key)


def translator(lang: str) -> Callable[[str], str]:
    """Return a `t(key)` bound to a (normalized) language code. Pure."""
    lang = normalize_lang(lang)
    return lambda key: translate(lang, key)
