-- 用户表
CREATE TABLE IF NOT EXISTS nursing_users (
    user_id    TEXT PRIMARY KEY,
    username   TEXT UNIQUE NOT NULL,
    name       TEXT NOT NULL,
    role       TEXT NOT NULL CHECK (role IN ('director','nursing_dept','logistics_dept','building','floor','general')),
    password   TEXT NOT NULL,
    dept       TEXT,
    building   TEXT,
    floor      TEXT
);

-- 老人档案
CREATE TABLE IF NOT EXISTS nursing_residents (
    id         TEXT PRIMARY KEY,
    name       TEXT NOT NULL,
    building   TEXT NOT NULL,
    floor      TEXT NOT NULL,
    room       TEXT NOT NULL,
    age        INTEGER,
    diagnosis  TEXT,
    care_level TEXT,
    notes      TEXT
);

-- 排班表
CREATE TABLE IF NOT EXISTS nursing_schedules (
    id         SERIAL PRIMARY KEY,
    staff_name TEXT NOT NULL,
    date       DATE NOT NULL,
    shift      TEXT NOT NULL,
    building   TEXT,
    floor      TEXT,
    zone       TEXT,
    task_note  TEXT
);

-- 护理工单
CREATE TABLE IF NOT EXISTS nursing_work_orders (
    id          SERIAL PRIMARY KEY,
    resident_id TEXT,
    date        DATE DEFAULT CURRENT_DATE,
    type        TEXT,
    completed   BOOLEAN DEFAULT FALSE,
    staff_name  TEXT,
    note        TEXT
);

-- 库存表
CREATE TABLE IF NOT EXISTS nursing_inventory (
    id           SERIAL PRIMARY KEY,
    item_name    TEXT NOT NULL,
    category     TEXT,
    quantity     INTEGER NOT NULL,
    unit         TEXT,
    safety_stock INTEGER,
    updated_at   TIMESTAMP DEFAULT NOW()
);

-- 健康预警
CREATE TABLE IF NOT EXISTS nursing_health_alerts (
    id          SERIAL PRIMARY KEY,
    resident_id TEXT,
    content     TEXT NOT NULL,
    category    TEXT,
    severity    TEXT DEFAULT 'info',
    created_at  TIMESTAMP DEFAULT NOW(),
    handled     BOOLEAN DEFAULT FALSE
);

-- 餐饮
CREATE TABLE IF NOT EXISTS nursing_meals (
    id        SERIAL PRIMARY KEY,
    date      DATE NOT NULL,
    meal_type TEXT NOT NULL,
    menu      TEXT NOT NULL
);

-- 活动
CREATE TABLE IF NOT EXISTS nursing_activities (
    id        SERIAL PRIMARY KEY,
    title     TEXT NOT NULL,
    date      DATE NOT NULL,
    time      TEXT,
    location  TEXT
);

-- 费用
CREATE TABLE IF NOT EXISTS nursing_finances (
    id          SERIAL PRIMARY KEY,
    resident_id TEXT,
    month       TEXT NOT NULL,
    amount      DECIMAL(10,2),
    paid         BOOLEAN DEFAULT FALSE
);

-- 投诉
CREATE TABLE IF NOT EXISTS nursing_complaints (
    id      SERIAL PRIMARY KEY,
    content TEXT NOT NULL,
    source  TEXT,
    status  TEXT DEFAULT 'pending'
);

